function reproducir(audioSrc, letra) {
    let player = document.getElementById("audioPlayer");
    let letraCancion = document.getElementById("letra-cancion");

    player.src = `audio/${audioSrc}`;
    player.play();

    if (letraCancion.textContent !== letra) {
        letraCancion.textContent = letra;
    }
}

